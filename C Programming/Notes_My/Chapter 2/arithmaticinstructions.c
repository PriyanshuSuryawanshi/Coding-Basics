#include <stdio.h>
int main() {
    float a = 8;
    float b = 3;
    int c = 40;
    int h = 5;

    printf("value of a + b = %f\n",a + b);
    printf("value of a - b = %f\n",a - b);
    printf("value of a * b = %f\n",a * b);
    printf("value of a / b = %f\n",a / b);
    printf("value of c % h = %d\n",c % h);
    printf("When c is divided by h we get %d as remainder\n",c % h);
return 0;
}